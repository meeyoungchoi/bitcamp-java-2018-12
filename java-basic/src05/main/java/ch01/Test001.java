package ch01;
public class Test001{

    public static void main(String[] args){


      
        
        //정수 78을 출력
        //10진수
        System.out.println("78");
        //2진수
        System.out.println(0b1001110);
        //8진수
        System.out.println(0116);
        //16진수
        System.out.println(0X4E);
        System.out.println(0x400);
        //부동소수점 출력
        //12.345
        //12.345
        System.out.println(12.345);
        //1.2345
        System.out.println(12.345E1);
        
        //0.12345
        System.out.println(12345E-5);
        
        //123.45
        System.out.println(12345E-2);
        
        //
        //논리값 출력
        //참출력
        System.out.println(true);
        //거짓 출력
        System.out.println(false);
        //문자 출력
        //0x61->A
        System.out.println((char)0x61);
        //97값을 출력했을때 a가 나오게 하라
        System.out.println((char)97);
        //작은 따옴표를 사요하여 a를 출력하라
        System.out.println('a');
        
        //문자열을 출력하라
        //Hello라는 문자열을 출력하라
        System.out.println("Hello");
        
        //안녕하세요 라는 문자열을 출력하라
        System.out.println("안녕하세요");
        
    }
}