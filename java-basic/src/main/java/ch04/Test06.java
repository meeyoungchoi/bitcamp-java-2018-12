//관계연산자(> >= < <=), 등위연산자(==, !=)
package ch04;

public class Test06 {

  public static void main(String[] args) {
  
    System.out.println(10 < 20);
    System.out.println(10 <= 20);
    System.out.println(10 > 20);
    System.out.println(10 >= 10);
    System.out.println(10 == 20); //l-value는 값이 올수 없다.
    System.out.println(10 != 20);
    
    
  }

}


